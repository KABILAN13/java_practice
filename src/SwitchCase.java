public class SwitchCase {
    public static void main(String[] args) {
        char attendance='B';
        switch (attendance){
            case 'A':
                System.out.println("No");
                break;
            case 'B':
                System.out.println("Yes");
                break;
            case 'C':
                System.out.println("No");
                break;
            default:
                System.out.println("No");
        }
        System.out.println("ends");
    }
}
